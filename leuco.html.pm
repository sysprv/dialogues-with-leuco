  ◊cloud{
Your fate is sealed. No one lifts his eyes to a goddess
with impunity.
  }
  ◊ixion{
Not even to the Goddess of the Oaks, the Lady of the
Peaks?
  }
  ◊cloud{
It doesn't matter which, Ixion. But don't be afraid.
I'll be with you till the end.
  }
